package entity;


public class ReturnVisit {

	private Integer rvID;
	private Integer clID;
	private String rvDate;
	private String rvReason;
	private String rvRecord;

	public Integer getRvID() {
		return rvID;
	}

	public void setRvID(Integer rvID) {
		this.rvID = rvID;
	}

	public Integer getClID() {
		return clID;
	}

	public void setClID(Integer clID) {
		this.clID = clID;
	}

	public String getRvDate() {
		return rvDate;
	}

	public void setRvDate(String rvDate) {
		this.rvDate = rvDate;
	}

	public String getRvReason() {
		return rvReason;
	}

	public void setRvReason(String rvReason) {
		this.rvReason = rvReason;
	}

	public String getRvRecord() {
		return rvRecord;
	}

	public void setRvRecord(String rvRecord) {
		this.rvRecord = rvRecord;
	}
}
